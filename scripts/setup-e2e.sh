# =============================================================================

main() {
    local action="${1:-setup}"

    case "$action" in
        --start)
            check_dependencies
            setup_env_file
            generate_ssl_certs
            generate_mtls_certs
            create_seed_sql
            start_docker_services
            setup_playwright
            log_success "E2E test environment ready!"
            echo ""
            echo "Test Credentials:"
            echo "  Admin: ${TEST_ADMIN_EMAIL} / ${TEST_ADMIN_PASSWORD}"
            echo "  Customer: ${TEST_CUSTOMER_EMAIL} / ${TEST_CUSTOMER_PASSWORD}"
            echo ""
            echo "Run tests with: cd tests/e2e && pnpm test"
            ;;
        --clean)
            cleanup
            ;;
        --seed-only)
            create_seed_sql
            docker compose exec -T postgres psql -U virtuestack -d virtuestack < "${PROJECT_ROOT}/migrations/test_seed.sql"
            log_success "Test data seeded"
            ;;
        setup|*)
            check_dependencies
            setup_env_file
            generate_ssl_certs
            generate_mtls_certs
            create_seed_sql
            setup_playwright
            log_success "E2E test environment configured!"
            echo ""
            echo "To start services, run: $0 --start"
            echo "To clean up, run: $0 --clean"
            ;;
    esac
}

main "$@"
