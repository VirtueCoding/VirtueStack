# =============================================================================
# SSL Certificate Generation
# =============================================================================

generate_ssl_certs() {
    log_info "Generating SSL certificates for testing..."

    local ssl_dir="${PROJECT_ROOT}/ssl"
    mkdir -p "$ssl_dir"

    # Generate self-signed certificate
    openssl req -x509 -nodes -days 365 -newkey rsa:2048 \
        -keyout "${ssl_dir}/key.pem" \
        -out "${ssl_dir}/cert.pem" \
        -subj "/C=US/ST=Test/L=Test/O=VirtueStack/OU=Testing/CN=localhost" \
        -addext "subjectAltName=DNS:localhost,DNS:*.localhost,IP:127.0.0.1" \
        2>/dev/null

    # Keep the private key owner-only. The nginx master process loads the key
    # before dropping privileges to its worker user.
    chmod 600 "${ssl_dir}/key.pem"
    chmod 644 "${ssl_dir}/cert.pem"

    log_success "Generated SSL certificates"
}

# =============================================================================
# mTLS Certificate Generation
# =============================================================================

generate_mtls_certs() {
    log_info "Generating mTLS certificates for Controller-Node Agent communication..."

    local certs_dir="${PROJECT_ROOT}/certs"
    mkdir -p "$certs_dir"

    # Generate CA
    openssl genrsa -out "${certs_dir}/ca.key" 4096 2>/dev/null
    openssl req -new -x509 -days 365 -key "${certs_dir}/ca.key" \
        -out "${certs_dir}/ca.crt" \
        -subj "/C=US/ST=Test/L=Test/O=VirtueStack/OU=CA/CN=VirtueStack Test CA" \
        2>/dev/null

    # Generate Controller certificate
    openssl genrsa -out "${certs_dir}/controller.key" 4096 2>/dev/null
    openssl req -new -key "${certs_dir}/controller.key" \
        -out "${certs_dir}/controller.csr" \
        -subj "/C=US/ST=Test/L=Test/O=VirtueStack/OU=Controller/CN=controller" \
        2>/dev/null
    openssl x509 -req -days 365 -in "${certs_dir}/controller.csr" \
        -CA "${certs_dir}/ca.crt" -CAkey "${certs_dir}/ca.key" -CAcreateserial \
        -out "${certs_dir}/controller.crt" \
        -extfile <(echo "subjectAltName=DNS:controller,DNS:localhost,IP:127.0.0.1") \
        2>/dev/null
    rm -f "${certs_dir}/controller.csr"

    # Generate Node Agent certificate
    openssl genrsa -out "${certs_dir}/node-agent.key" 4096 2>/dev/null
    openssl req -new -key "${certs_dir}/node-agent.key" \
        -out "${certs_dir}/node-agent.csr" \
        -subj "/C=US/ST=Test/L=Test/O=VirtueStack/OU=Node Agent/CN=node-agent" \
        2>/dev/null
    openssl x509 -req -days 365 -in "${certs_dir}/node-agent.csr" \
        -CA "${certs_dir}/ca.crt" -CAkey "${certs_dir}/ca.key" -CAcreateserial \
        -out "${certs_dir}/node-agent.crt" \
        -extfile <(echo "subjectAltName=DNS:node-agent,DNS:localhost,IP:127.0.0.1") \
        2>/dev/null
    rm -f "${certs_dir}/node-agent.csr"

    # Set permissions
    chmod 600 "${certs_dir}"/*.key
    chmod 644 "${certs_dir}"/*.crt

    log_success "Generated mTLS certificates"
}

