run_seed_sql() {
    local seed_path="${1:-${PROJECT_ROOT}/migrations/test_seed.sql}"
    local database_url
    local container_database_url

    load_env_file
    database_url="$(build_database_url)"

    if command -v psql >/dev/null 2>&1; then
        psql "${database_url}" -v ON_ERROR_STOP=1 -f "${seed_path}"
        return
    fi

    require_commands docker
    container_database_url="postgresql://${POSTGRES_USER:-virtuestack}:${POSTGRES_PASSWORD}@postgres:5432/${POSTGRES_DB:-virtuestack}?sslmode=disable"
    docker run --rm \
        --network virtuestack-test-network \
        -v "${PROJECT_ROOT}/migrations:/migrations:ro" \
        postgres:18-alpine \
        psql "${container_database_url}" -v ON_ERROR_STOP=1 -f "/migrations/$(basename "${seed_path}")"
}

# =============================================================================
# Docker Services
# =============================================================================

start_docker_services() {
    log_info "Starting Docker services..."
    require_commands docker
    load_env_file

    cd "$PROJECT_ROOT"

    # Build images
    "${DOCKER_COMPOSE[@]}" build

    # Start services
    "${DOCKER_COMPOSE[@]}" up -d

    # Wait for services to be healthy
    log_info "Waiting for services to be healthy..."
    sleep 10

    # Check PostgreSQL
    for i in {1..30}; do
        if "${DOCKER_COMPOSE[@]}" exec -T postgres pg_isready -U "${POSTGRES_USER:-virtuestack}" -d "${POSTGRES_DB:-virtuestack}" 2>/dev/null; then
            log_success "PostgreSQL is ready"
            break
        fi
        sleep 1
    done

    # Check NATS
    for i in {1..30}; do
        if "${DOCKER_COMPOSE[@]}" exec -T nats wget --spider -q http://localhost:8222/healthz 2>/dev/null; then
            log_success "NATS is ready"
            break
        fi
        sleep 1
    done

    # Check Controller
    for i in {1..30}; do
        if curl -sf http://localhost:8080/health > /dev/null 2>&1; then
            log_success "Controller is ready"
            break
        fi
        sleep 1
    done

    # Run migrations
    log_info "Running database migrations..."
    docker run --rm --network virtuestack-test-network \
        --env-file "${PROJECT_ROOT}/.env.test" \
        -v "${PROJECT_ROOT}/migrations:/migrations:ro" \
        migrate/migrate:v4.19.0 \
        -path /migrations \
        -database "postgresql://${POSTGRES_USER:-virtuestack}:${POSTGRES_PASSWORD}@postgres:5432/${POSTGRES_DB:-virtuestack}?sslmode=disable" up

    # Seed test data
    log_info "Seeding test data..."
    "${DOCKER_COMPOSE[@]}" exec -T postgres \
        psql -U "${POSTGRES_USER:-virtuestack}" -d "${POSTGRES_DB:-virtuestack}" \
        -v ON_ERROR_STOP=1 -f /docker-entrypoint-initdb.d/migrations/test_seed.sql

    log_success "All services started and healthy"
}

stop_docker_services() {
    log_info "Stopping Docker services..."
    require_commands docker

    cd "$PROJECT_ROOT"
    "${DOCKER_COMPOSE[@]}" down -v

    log_success "Docker services stopped"
}

# =============================================================================
# Playwright Setup
# =============================================================================

setup_playwright() {
    log_info "Setting up Playwright..."

    cd "${PROJECT_ROOT}/tests/e2e"

    # Install dependencies
    if [ -f package.json ]; then
        corepack enable
        pnpm install --frozen-lockfile
        pnpm exec playwright install --with-deps
    fi

    # Create auth directory
    mkdir -p .auth

    log_success "Playwright setup complete"
}

# =============================================================================
# Cleanup
# =============================================================================

cleanup() {
    log_warning "Cleaning up E2E test environment..."

    # Stop Docker services
    stop_docker_services

    # Remove generated files
    rm -f "${PROJECT_ROOT}/.env.test"
    rm -f "${PROJECT_ROOT}/migrations/test_seed.sql"
    rm -rf "${PROJECT_ROOT}/ssl"
    rm -rf "${PROJECT_ROOT}/certs"
    rm -rf "${PROJECT_ROOT}/tests/e2e/.auth"
    rm -rf "${PROJECT_ROOT}/tests/e2e/playwright-report"
    rm -rf "${PROJECT_ROOT}/tests/e2e/test-results"

    log_success "Cleanup complete"
}

# =============================================================================
# Main
# =============================================================================

main() {
    local action="${1:-setup}"

    case "$action" in
        --start)
            check_dependencies
            setup_env_file
            generate_ssl_certs
            generate_mtls_certs
            create_seed_sql
            start_docker_services
            setup_playwright
            log_success "E2E test environment ready!"
            echo ""
            echo "Test Credentials:"
            echo "  Admin: ${TEST_ADMIN_EMAIL} / ${TEST_ADMIN_PASSWORD}"
            echo "  Customer: ${TEST_CUSTOMER_EMAIL} / ${TEST_CUSTOMER_PASSWORD}"
            echo ""
            echo "Run tests with: cd tests/e2e && pnpm test"
            ;;
        --clean)
            cleanup
            ;;
        --seed-only)
            create_seed_sql
            # Seed-only intentionally uses direct psql execution via run_seed_sql
            # instead of docker compose exec, so it can target any reachable
            # Postgres configured by .env.test or fall back to a one-shot psql container.
            run_seed_sql "${PROJECT_ROOT}/migrations/test_seed.sql"
            log_success "Test data seeded"
            ;;
        setup|*)
            check_dependencies
            setup_env_file
            generate_ssl_certs
            generate_mtls_certs
            create_seed_sql
            setup_playwright
            log_success "E2E test environment configured!"
            echo ""
            echo "To start services, run: $0 --start"
            echo "To clean up, run: $0 --clean"
            ;;
    esac
}

main "$@"
