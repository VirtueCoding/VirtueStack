# =============================================================================
# Docker Services
# =============================================================================

start_docker_services() {
    log_info "Starting Docker services..."

    cd "$PROJECT_ROOT"

    # Copy test env to .env for docker-compose
    if [ -f .env.test ]; then
        cp .env.test .env
    fi

    # Build images
    docker compose build

    # Start services
    docker compose up -d

    # Wait for services to be healthy
    log_info "Waiting for services to be healthy..."
    sleep 10

    # Check PostgreSQL
    for i in {1..30}; do
        if docker compose exec -T postgres pg_isready -U virtuestack -d virtuestack 2>/dev/null; then
            log_success "PostgreSQL is ready"
            break
        fi
        sleep 1
    done

    # Check NATS
    for i in {1..30}; do
        if docker compose exec -T nats wget --spider -q http://localhost:8222/healthz 2>/dev/null; then
            log_success "NATS is ready"
            break
        fi
        sleep 1
    done

    # Check Controller
    for i in {1..30}; do
        if curl -sf http://localhost:8080/health > /dev/null 2>&1; then
            log_success "Controller is ready"
            break
        fi
        sleep 1
    done

    # Run migrations
    log_info "Running database migrations..."
    docker compose exec -T controller migrate -path /migrations -database "postgresql://virtuestack:virtuestack_test_password@postgres:5432/virtuestack?sslmode=disable" up

    # Seed test data
    log_info "Seeding test data..."
    docker compose exec -T postgres psql -U virtuestack -d virtuestack -f /docker-entrypoint-initdb.d/../migrations/test_seed.sql 2>/dev/null || \
        docker compose exec -T postgres psql -U virtuestack -d virtuestack < "${PROJECT_ROOT}/migrations/test_seed.sql"

    log_success "All services started and healthy"
}

stop_docker_services() {
    log_info "Stopping Docker services..."

    cd "$PROJECT_ROOT"
    docker compose down -v

    log_success "Docker services stopped"
}

# =============================================================================
# Playwright Setup
# =============================================================================

setup_playwright() {
    log_info "Setting up Playwright..."

    cd "${PROJECT_ROOT}/tests/e2e"

    # Install dependencies
    if [ -f package.json ]; then
        corepack enable
        pnpm install --frozen-lockfile --ignore-scripts
        pnpm exec playwright install --with-deps
    fi

    # Create auth directory
    mkdir -p .auth

    log_success "Playwright setup complete"
}

# =============================================================================
# Cleanup
# =============================================================================

cleanup() {
    log_warning "Cleaning up E2E test environment..."

    # Stop Docker services
    stop_docker_services

    # Remove generated files
    rm -f "${PROJECT_ROOT}/.env.test"
    rm -f "${PROJECT_ROOT}/migrations/test_seed.sql"
    rm -rf "${PROJECT_ROOT}/ssl"
    rm -rf "${PROJECT_ROOT}/certs"
    rm -rf "${PROJECT_ROOT}/tests/e2e/.auth"
    rm -rf "${PROJECT_ROOT}/tests/e2e/playwright-report"
    rm -rf "${PROJECT_ROOT}/tests/e2e/test-results"

    log_success "Cleanup complete"
}

